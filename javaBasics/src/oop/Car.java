package oop;

public class Car {
	String brand , color ;
	int speed;
	
	public Car(String brand, String color) {
		this.brand = brand;
		this.color = color;
		this.speed = 0;
	}
	public Car(String brand, String color, int speed) {
		this.brand = brand;
		this.color = color;
		this.speed = speed;
	}
	

}
